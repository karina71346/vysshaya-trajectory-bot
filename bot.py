async def process_name(message: types.Message, state: FSMContext):
    """–°–æ—Ö—Ä–∞–Ω—è–µ–º –∏–º—è –∏ –≤–µ–¥—ë–º –≤ –∫–∞–Ω–∞–ª."""
    full_name = (message.text or "").strip()
    if not full_name:
        await message.answer("–ù–∞–ø–∏—à–∏—Ç–µ, –ø–æ–∂–∞–ª—É–π—Å—Ç–∞, –∏–º—è –∏ —Ñ–∞–º–∏–ª–∏—é –æ–¥–Ω–∏–º —Å–æ–æ–±—â–µ–Ω–∏–µ–º.")
        return

    await state.update_data(full_name=full_name)

    text = (
        f"{full_name}, —á—Ç–æ–±—ã –ø–æ–ª—É—á–∏—Ç—å –¥–æ—Å—Ç—É–ø –∫ –º–∞—Ç–µ—Ä–∏–∞–ª–∞–º, –≤–∞–∂–Ω–æ –±—ã—Ç—å –≤ –∫–∞–Ω–∞–ª–µ ¬´–ë–∏–∑–Ω–µ—Å —Å–æ —Å–º—ã—Å–ª–æ–º¬ª.\n\n"
        "–ü–µ—Ä–µ–π–¥–∏—Ç–µ –≤ –∫–∞–Ω–∞–ª –ø–æ –∫–Ω–æ–ø–∫–µ –Ω–∏–∂–µ, –∑–∞—Ç–µ–º –≤–µ—Ä–Ω–∏—Ç–µ—Å—å –≤ –±–æ—Ç –∏ –Ω–∞–∂–º–∏—Ç–µ ¬´–Ø –≤ –∫–∞–Ω–∞–ª–µ ‚úÖ¬ª."
    )

    await message.answer(text, reply_markup=channel_check_kb())
    await state.set_state(Registration.waiting_for_channel_confirm)


async def channel_joined(callback: types.CallbackQuery, state: FSMContext, bot: Bot):
    """–ü—Ä–æ–≤–µ—Ä—è–µ–º –ø–æ–¥–ø–∏—Å–∫—É –∏ –≤—ã–¥–∞—ë–º –≥–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é."""
    await callback.answer()
    user_id = callback.from_user.id

    try:
        member = await bot.get_chat_member(CHANNEL_USERNAME, user_id)
    except Exception as e:
        logger.exception("–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø—Ä–æ–≤–µ—Ä–∫–µ –ø–æ–¥–ø–∏—Å–∫–∏: %s", e)
        await callback.message.answer(
            "–ù–µ –ø–æ–ª—É—á–∏–ª–æ—Å—å –ø—Ä–æ–≤–µ—Ä–∏—Ç—å –ø–æ–¥–ø–∏—Å–∫—É. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â—ë —Ä–∞–∑ —á—É—Ç—å –ø–æ–∑–∂–µ."
        )
        return

    if member.status not in {
        ChatMemberStatus.MEMBER,
        ChatMemberStatus.CREATOR,
        ChatMemberStatus.ADMINISTRATOR,
    }:
        await callback.message.answer(
            "–ü–æ—Ö–æ–∂–µ, –≤—ã –µ—â—ë –Ω–µ –ø–æ–¥–ø–∏—Å–∞–Ω—ã –Ω–∞ –∫–∞–Ω–∞–ª. –ü–æ–¥–ø–∏—à–∏—Ç–µ—Å—å –∏ –Ω–∞–∂–º–∏—Ç–µ –∫–Ω–æ–ø–∫—É —Å–Ω–æ–≤–∞."
        )
        return

    data = await state.get_data()
    full_name = data.get("full_name") or callback.from_user.full_name

    await callback.message.answer(
        f"–ë–ª–∞–≥–æ–¥–∞—Ä—é, {full_name}! –¢–µ–ø–µ—Ä—å –≤—ã –≤ —Å–æ–æ–±—â–µ—Å—Ç–≤–µ –ª–∏–¥–µ—Ä–æ–≤.\n\n"
        "–ù–∏–∂–µ ‚Äî –≥–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é. –í—ã–±–µ—Ä–∏—Ç–µ, —Å —á–µ–≥–æ –Ω–∞—á–Ω—ë–º.",
        reply_markup=main_menu_kb(),
    )
    await state.set_state(Registration.registered)


